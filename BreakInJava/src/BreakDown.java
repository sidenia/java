
public class BreakDown {

	public static void main(String[] args) {
		//ex 1
		while (true) {
			System.out.println("Hi!");
			break;
		}
		
		
		System.out.println("-----------------------");
		
		//ex2
		
		int i = 0;
		
		while (i <3){
			System.out.println("Hi!");
			i++;
			break; //it ends in the first time;
		}
		
		
	}

}
