
public class InitializationVsDeclaration {

	public static void main(String[] args) {
	
		String pizza1 = "pineapple"; //initialization  =  type, name and value in the same line.
		String pizza2; 				//declaration = just type and name for a variable but no value yet.
		pizza2 = "bbq";				//assignment = you use the assignment signal and ad a value
	}
}
