
public class Exponents {

	public static void main(String[] args) {
		//pow para potencia -  sqrt para raíz quadrada
		System.out.println((int)Math.pow(5,2)); //o int é para exibir apenas 25 ao invés de 25.0 no resultado
		System.out.println(Math.pow(5.1, 2.2));
		
		System.out.println((int)Math.sqrt(25));
		
		double exponent = Math.pow(5, 2);
		System.out.println(exponent);
		
		int expo = (int)Math.pow(5, 2);
		System.out.println(expo);
	}

}
