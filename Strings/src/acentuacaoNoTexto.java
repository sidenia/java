
public class acentuacaoNoTexto {
	public static void main(String[] args) {
		System.out.println("Sidenia is \"calm\". Do you agree?");
		
		//com quotes, adiciona a barra invertida onde começa e onde termina.
		System.out.println("These are double quotes \"\""); 
		System.out.println("This is a backslash \\");
		
		//
		System.out.println("What does \"feijoada\" means?");
		System.out.println("The file is located at ~ \\Desktop\\folder\\Si");
		System.out.println("This is a tab \t sidenia");  //insere um tab no texto.
		System.out.println("This is \n understand?"); //insere um enter no texto
		System.out.println("");
	}
}
