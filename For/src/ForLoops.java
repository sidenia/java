
public class ForLoops {

	public static void main(String[] args) {
		
		for(int i = 0; i < 3; i++ ) {
			System.out.println("LOVE");
		}
		
		int [] grades = {98, 100, 83, 98, 93};
				//index  0   1    2   3   4
		
		for (int i = 0; i < grades.length; i++) {
			System.out.println(grades[i]);
		}
	}

}
